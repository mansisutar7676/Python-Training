# While loop
i=1
while i<=5:
    print(i, end=" ")
    i+=1
print("\n")

# For loop
str = 'Mansi'
for i in str:
    print(i, end=" ")
print("\n")

list = [1,2,3,4,5]
for i in list:
    print(i, end=" ")
print("\n")

# For range
for i in range(10):
    print(i, end=" ")
print()

for i in range(1,5):
    print(i, end=" ")
print()

for i in range(1,10,2):
    print(i, end=" ")
