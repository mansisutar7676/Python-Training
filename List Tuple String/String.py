# Accessing characters string
String1 = "MansiSutar"
print(String1)

# Printing First character
print(String1[0])

# Printing Last character
print(String1[-1])

# String Slicing

print(String1[1:10])

print(String1[3:-2])

# Formatting of Strings
# Default order
Str = "{} {} {}".format('Siya', 'jiya', 'Meera')
print(Str)

# Positional Formatting
Str = "{1} {0} {2}".format('Siya', 'jiya', 'Meera')
print(Str)

# Keyword Formatting
Str = "{l} {f} {g}".format(g='Jiya', f='Siya', l='Meera')
print(String1)